import http from "k6/http";
import { check, sleep } from "k6";
import { Counter, Rate } from "k6/metrics";

const aiSuccessRate = new Rate("ai_success_rate");
const aiFailureCount = new Counter("ai_failure_count");

const API_URL = __ENV.API_URL || "http://localhost:8080/api/v1";
const TENANT_SLUG = __ENV.TENANT_SLUG || "default";
const EMAIL = __ENV.EMAIL || "guru@xamina.local";
const PASSWORD = __ENV.PASSWORD || "Guru123!";

export const options = {
  vus: Number(__ENV.VUS || 1),
  duration: __ENV.DURATION || "65s",
  thresholds: {
    checks: ["rate>0.95"],
    ai_success_rate: ["rate>0.90"],
    http_req_duration: ["p(95)<2500"],
  },
};

export function setup() {
  const loginRes = http.post(
    `${API_URL}/auth/login`,
    JSON.stringify({
      tenant_slug: TENANT_SLUG,
      email: EMAIL,
      password: PASSWORD,
    }),
    { headers: { "Content-Type": "application/json" } },
  );
  check(loginRes, {
    "login status is 200": (r) => r.status === 200,
  });

  const body = loginRes.json();
  const token = body?.data?.access_token;
  if (!token) {
    throw new Error("missing access token from login response");
  }
  return { token };
}

export default function (data) {
  const payload = {
    topic: "K6 Baseline",
    context: "Generated by k6 baseline test",
    question_type: "multiple_choice",
    difficulty: "medium",
    count: 1,
  };

  const res = http.post(`${API_URL}/ai/generate`, JSON.stringify(payload), {
    headers: {
      Authorization: `Bearer ${data.token}`,
      "Content-Type": "application/json",
    },
  });

  const ok = check(res, {
    "generate status is 200": (r) => r.status === 200,
    "generate payload has questions": (r) => {
      const parsed = r.json();
      return Array.isArray(parsed?.data?.questions);
    },
  });
  aiSuccessRate.add(ok ? 1 : 0);
  if (!ok) {
    aiFailureCount.add(1);
  }

  sleep(Number(__ENV.SLEEP_SECONDS || 5.5));
}
